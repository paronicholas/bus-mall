// getter tags
function getSectionIdTag(idName){
  var sectionIdTag = document.getElementById(idName);
  return sectionIdTag;
}

function getUlIdTag(idName){
  var ulIdTag = document.getElementById(idName);
  return ulIdTag;
}

function getChartIdTag(idName){
  var chartIdTag = document.getElementById(idName);
  return chartIdTag;
}

// setter tags
function setDivTag(){
  var divTag = document.createElement('div');
  return divTag;
}

function setImageTag(){
  var imageTag = document.createElement('img');
  return imageTag;
}

function setH3Tag(){
  var imageH3 = document.createElement('h3');
  return imageH3;
}